public class Cuadrilatero extends Poligono{
    
    private float base,altura;

    public Cuadrilatero(float base, float altura) {
        this.base = base;
        this.altura = altura;
    }

    public Cuadrilatero() {
    }

 
 

  

    public float getBase() {
        return base;
    }

    public float getAltura() {
        return altura;
    }

   

  
    
    
  
    
    
    
    
}
